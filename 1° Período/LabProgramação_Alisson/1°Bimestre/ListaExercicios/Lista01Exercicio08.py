"""Faça um algoritmo para calcular quantas ferraduras são necessárias para equipar todos os cavalos
comprados para um haras.
"""

qtde_cavalos= int(input('Informe a quantidade de cavalos: '))

ferraduras = qtde_cavalos*4

print(f'Para equipar {qtde_cavalos} cavalos, será necessário {ferraduras} ferraduras.')